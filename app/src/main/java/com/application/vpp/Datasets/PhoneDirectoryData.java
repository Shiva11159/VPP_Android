package com.application.vpp.Datasets;

public class PhoneDirectoryData {
    private String name;
    private String number;


    public PhoneDirectoryData(String name,String number){

        this.name = name;
        this.number = number;

    }

    public String getName() {
        return name;
    }

    public String getNumber() {
        return number;
    }
}
